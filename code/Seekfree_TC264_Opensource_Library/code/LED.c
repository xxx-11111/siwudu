#include "zf_common_headfile.h"
#include "led.h"
#include "define.h"

//------------------------------------------------------------------------------------------------------------------
// 函数简介     初始化led灯引脚
// 参数说明     void
// 返回参数     void
// 备注信息     默认高电平，即不亮
//------------------------------------------------------------------------------------------------------------------
void led_init(void)
{
    gpio_init (led_2, GPO, 1, GPO_PUSH_PULL);
    gpio_init (led_3, GPO, 1, GPO_PUSH_PULL);
    gpio_init (led_4, GPO, 1, GPO_PUSH_PULL);
}


//------------------------------------------------------------------------------------------------------------------
// 函数简介     led2亮
// 参数说明     void
// 返回参数     void
// 备注信息
//------------------------------------------------------------------------------------------------------------------
void led_2_on(void)
{
    gpio_set_level(led_2,0);
}


//------------------------------------------------------------------------------------------------------------------
// 函数简介     led2灭
// 参数说明     void
// 返回参数     void
// 备注信息
//------------------------------------------------------------------------------------------------------------------
void led_2_off(void)
{
    gpio_set_level(led_2,1);
}


